n1=int(input('enter 1st number : '))
n2=int(input('enter 2nd number : '))
oper=input('enter operation : ')
if oper=='+':
    print('addition is',(n1+n2))
elif oper=='-':
    print('subtraction is',(n1-n2))
elif oper=='*':
    print('product is',(n1*n2))
elif oper=='/':
    print('remainder is',(n1/n2))
